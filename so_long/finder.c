#include "so_long.h"

void ft_finder(t_variable *p)
{
    p->i = 0;
    p->j = 0;

    while (p->p[p->i])
    {
        p->j = 0;
        while (p->p[p->i][p->j])
        {
            if (p->p[p->i][p->j] == 'P')
            {
                return;
            }
                p->j++;
        }
        p->i++;
    }
}