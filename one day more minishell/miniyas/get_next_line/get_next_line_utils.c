#include "get_next_line.h"

char	*ft_strdup(char *s1)
{
	char	*str;
	size_t	i;

	str = (char *)ft_calloc(ft_strlen(s1) + 1, sizeof(char));
	i = 0;
	if (!str)
		return (0);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	return (str);
}
